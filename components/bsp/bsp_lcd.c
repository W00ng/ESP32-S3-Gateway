#include "bsp_lcd.h"

static const char *TAG = "bsp_lcd";

esp_lcd_panel_handle_t lcd_panel = NULL;

esp_err_t bsp_lcd_init(void)
{
    ESP_LOGI(TAG, "bsp_lcd_init start");

    gpio_config_t bk_gpio_config = {
        .mode = GPIO_MODE_OUTPUT,
        .pin_bit_mask = 1ULL << GPIO_LCD_BL
    };
    ESP_ERROR_CHECK(gpio_config(&bk_gpio_config));
    // Turn off backlight to avoid unpredictable display on the LCD screen while initializing
    ESP_ERROR_CHECK(gpio_set_level(GPIO_LCD_BL, LCD_BL_OFF_LEVEL));

    spi_bus_config_t bus_cfg = {
        .sclk_io_num = GPIO_LCD_SCK,
        .mosi_io_num = GPIO_LCD_SDA,
        .miso_io_num = GPIO_NUM_NC,
        .max_transfer_sz = LCD_WIDTH * LCD_HEIGHT * sizeof(uint16_t),
    };
    ESP_ERROR_CHECK(spi_bus_initialize(LCD_SPI_HOST, &bus_cfg, SPI_DMA_CH_AUTO));

    esp_lcd_panel_io_handle_t io_handle = NULL;
    esp_lcd_panel_io_spi_config_t io_config = {
        .dc_gpio_num = GPIO_LCD_DC,
        .cs_gpio_num = GPIO_LCD_CS,
        .pclk_hz = LCD_SPI_CLOCK_HZ,
        .lcd_cmd_bits = LCD_CMD_BITS,
        .lcd_param_bits = LCD_PARAM_BITS,
        .spi_mode = 0,
        .trans_queue_depth = 10,
        // .on_color_trans_done = lcd_trans_done_cb,
        // .user_ctx = NULL,
    };
    ESP_ERROR_CHECK(esp_lcd_new_panel_io_spi((esp_lcd_spi_bus_handle_t)LCD_SPI_HOST, &io_config, &io_handle));

    esp_lcd_panel_dev_config_t panel_config = {
        .reset_gpio_num = GPIO_LCD_RST,
        .color_space = ESP_LCD_COLOR_SPACE_RGB,
        // .color_space = ESP_LCD_COLOR_SPACE_BGR,
        .bits_per_pixel = 16,
    };
    ESP_ERROR_CHECK(esp_lcd_new_panel_st7789(io_handle, &panel_config, &lcd_panel));

    /**
     * @brief Configure LCD rotation and mirror
     */
    ESP_ERROR_CHECK(esp_lcd_panel_reset(lcd_panel));
    ESP_ERROR_CHECK(esp_lcd_panel_init(lcd_panel));
    ESP_ERROR_CHECK(esp_lcd_panel_set_gap(lcd_panel, 0, 0));
    ESP_ERROR_CHECK(esp_lcd_panel_invert_color(lcd_panel, false));
    ESP_ERROR_CHECK(esp_lcd_panel_swap_xy(lcd_panel, true));
    ESP_ERROR_CHECK(esp_lcd_panel_mirror(lcd_panel, true, false));

    // Turn on backlight (Different LCD screens may need different levels)
    ESP_ERROR_CHECK(gpio_set_level(GPIO_LCD_BL, LCD_BL_ON_LEVEL));

    lcd_clear_fast(lcd_panel, COLOR_WHITE);
    // lcd_clear_fast(lcd_panel, COLOR_RED);
    // lcd_clear_fast(lcd_panel, COLOR_GREEN);
    // lcd_clear_fast(lcd_panel, COLOR_BLUE);
    // lcd_clear_fast(lcd_panel, COLOR_BLACK);
    // while (1)
    // {
    //     lcd_clear_fast(lcd_panel, COLOR_WHITE);
    //     vTaskDelay(pdMS_TO_TICKS(2000));
    //     lcd_clear_fast(lcd_panel, COLOR_RED);
    //     vTaskDelay(pdMS_TO_TICKS(2000));
    //     lcd_clear_fast(lcd_panel, COLOR_GREEN);
    //     vTaskDelay(pdMS_TO_TICKS(2000));
    //     lcd_clear_fast(lcd_panel, COLOR_BLUE);
    //     vTaskDelay(pdMS_TO_TICKS(2000));
    //     lcd_clear_fast(lcd_panel, COLOR_BLACK);
    //     vTaskDelay(pdMS_TO_TICKS(2000));
    // }
    lcd_draw_touch_point(lcd_panel, 20, 20, COLOR_RED);
    lcd_draw_touch_point(lcd_panel, 220, 220, COLOR_RED);

    return ESP_OK;
}

esp_err_t lcd_clear(esp_lcd_panel_handle_t panel, uint16_t color)
{
#if 1  //Swap the 2 bytes of RGB565 color
    uint16_t temp = color;
    color = temp >> 8 | temp << 8;
#endif
    uint16_t *buffer = heap_caps_malloc(LCD_WIDTH * sizeof(uint16_t), MALLOC_CAP_DMA );
    if (NULL == buffer) {
        ESP_LOGE(TAG, "Memory for bitmap is not enough");
        return ESP_FAIL;
    } else {
        for (uint16_t i = 0; i < LCD_WIDTH; i++) {
            buffer[i] = color;
        }
        for (int y = 0; y < LCD_HEIGHT; y++) {
            esp_lcd_panel_draw_bitmap(panel, 0, y, LCD_WIDTH, y+1, buffer);
        }
        heap_caps_free(buffer);
    }
    return ESP_OK;
}

esp_err_t lcd_clear_fast(esp_lcd_panel_handle_t panel, uint16_t color)
{
#if 1  //Swap the 2 bytes of RGB565 color
    uint16_t temp = color;
    color = temp >> 8 | temp << 8;
#endif
    uint16_t fact = 40;
    // uint64_t s = esp_timer_get_time();
    uint16_t *buffer = heap_caps_malloc(LCD_WIDTH * fact * sizeof(uint16_t), MALLOC_CAP_DMA);
    if (NULL == buffer) {
        ESP_LOGE(TAG, "Memory for bitmap is not enough");
        return ESP_FAIL;
    } else {
        for (uint16_t i = 0; i < LCD_WIDTH * fact; i++) {
            buffer[i] = color;
        }
        for (int y = 0; y < LCD_HEIGHT; y += fact) {
            esp_lcd_panel_draw_bitmap(panel, 0, y, LCD_WIDTH, y + fact, buffer);
        }
        heap_caps_free(buffer);
    }
    // uint64_t t1 = (esp_timer_get_time() - s);
    // float time_per_frame = t1 / 1000;   
    // float fps = 1000000.f / (float)t1;
    // ESP_LOGI(TAG, "@resolution %ux%u  [time per frame=%.2fMS, fps=%.2f]", LCD_WIDTH, LCD_HEIGHT, time_per_frame, fps);
    return ESP_OK;
}

esp_err_t lcd_draw_point(esp_lcd_panel_handle_t panel, uint16_t x, uint16_t y, uint16_t color)
{
    esp_lcd_panel_draw_bitmap(panel, x, y, x+1, y+1, &color);
    return ESP_OK;
}

esp_err_t lcd_draw_circle(esp_lcd_panel_handle_t panel, uint16_t x0, uint16_t y0, uint8_t r, uint16_t color)
{
	int a = 0, b = r;
	  
	while(a <= b)
	{
		esp_lcd_panel_draw_bitmap(panel, x0-b, y0-a, x0-b+1, y0-a+1, &color);             //3           
		esp_lcd_panel_draw_bitmap(panel, x0+b, y0-a, x0+b+1, y0-a+1, &color);             //0           
		esp_lcd_panel_draw_bitmap(panel, x0-a, y0+b, x0-a+1, y0+b+1, &color);             //1                
		esp_lcd_panel_draw_bitmap(panel, x0-a, y0-b, x0-a+1, y0-b+1, &color);             //2             
		esp_lcd_panel_draw_bitmap(panel, x0+b, y0+a, x0+b+1, y0+a+1, &color);             //4               
		esp_lcd_panel_draw_bitmap(panel, x0+a, y0-b, x0+a+1, y0-b+1, &color);             //5
		esp_lcd_panel_draw_bitmap(panel, x0+a, y0+b, x0+a+1, y0+b+1, &color);             //6 
		esp_lcd_panel_draw_bitmap(panel, x0-b, y0+a, x0-b+1, y0+a+1, &color);             //7
		a++;
		if((a*a+b*b)>(r*r))
		{
			b--;
		}
	}
    return ESP_OK;
}

